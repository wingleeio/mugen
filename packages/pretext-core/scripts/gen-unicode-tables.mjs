// Generates cpp/pretext/tables/unicode_tables.hpp from the EXACT regexes the
// TS sources use, evaluated on this Node's Unicode data. Do not hand-edit the
// output; add classes here and re-run:
//   node scripts/gen-unicode-tables.mjs
//
// Classes come from:
//   @chenglou/pretext src/analysis.ts:   \p{M}, \p{Nd}, [\p{P}\p{S}\p{Co}],
//                                        \p{Emoji_Presentation}, \p{Script=Arabic}
//   pretext-native segmenter/word.ts:    [\p{L}\p{M}], \p{Nd}, [\p{L}\p{M}\p{Nd}\p{Pc}], \s
//   pretext-native segmenter/grapheme.ts: \p{Extended_Pictographic} (if used)

const CLASSES = [
  ['Mark', /\p{M}/u],
  ['DecimalNumber', /\p{Nd}/u],
  ['Letter', /\p{L}/u],
  ['ConnectorPunctuation', /\p{Pc}/u],
  ['WordInternalSymbol', /[\p{P}\p{S}\p{Co}]/u],
  ['EmojiPresentation', /\p{Emoji_Presentation}/u],
  ['ExtendedPictographic', /\p{Extended_Pictographic}/u],
  ['RegionalIndicator', /\p{Regional_Indicator}/u],
  ['ScriptArabic', /\p{Script=Arabic}/u],
  // word.ts isWhitespace: /\s/.test(String.fromCodePoint(cp)) — \s is the JS
  // regex whitespace set (BMP-only), identical with or without the u flag.
  ['Whitespace', /\s/u],
];

function rangesFor(re) {
  const ranges = [];
  let start = -1;
  for (let cp = 0; cp <= 0x10ffff; cp++) {
    // Skip lone surrogates: not representable as JS string code points anyway.
    const isSurrogate = cp >= 0xd800 && cp <= 0xdfff;
    const match = !isSurrogate && re.test(String.fromCodePoint(cp));
    if (match && start < 0) start = cp;
    if (!match && start >= 0) {
      ranges.push([start, cp - 1]);
      start = -1;
    }
  }
  if (start >= 0) ranges.push([start, 0x10ffff]);
  return ranges;
}

let out = `// GENERATED by scripts/gen-unicode-tables.mjs on Node ${process.version}
// (V8 Unicode data). DO NOT EDIT BY HAND.
#pragma once

#include <cstdint>

namespace pretext::tables {

struct CpRange { uint32_t lo, hi; };

inline bool inRanges(char32_t cp, const CpRange* ranges, int n) {
  int lo = 0, hi = n - 1;
  while (lo <= hi) {
    int mid = (lo + hi) >> 1;
    if (cp < ranges[mid].lo) hi = mid - 1;
    else if (cp > ranges[mid].hi) lo = mid + 1;
    else return true;
  }
  return false;
}

`;

for (const [name, re] of CLASSES) {
  const ranges = rangesFor(re);
  out += `// ${re.source} — ${ranges.length} ranges\n`;
  out += `inline constexpr CpRange k${name}[] = {\n`;
  for (let i = 0; i < ranges.length; i += 6) {
    out +=
      '    ' +
      ranges
        .slice(i, i + 6)
        .map(([a, b]) => `{0x${a.toString(16)}, 0x${b.toString(16)}}`)
        .join(', ') +
      ',\n';
  }
  out += `};\n`;
  out += `inline bool is${name}(char32_t cp) { return inRanges(cp, k${name}, ${ranges.length}); }\n\n`;
}

out += `}  // namespace pretext::tables\n`;

import { writeFileSync, mkdirSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

const root = dirname(dirname(fileURLToPath(import.meta.url)));
const dest = join(root, 'cpp/pretext/tables/unicode_tables.hpp');
mkdirSync(dirname(dest), { recursive: true });
writeFileSync(dest, out);
console.log(`wrote ${dest}`);
