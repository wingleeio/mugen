// Generates cpp/pretext/tables/bidi_data.hpp from the EXACT data shipped in
// @chenglou/pretext@0.0.8 src/generated/bidi-data.ts (via the compiled dist).
// Do not hand-edit the output; re-run:
//   node scripts/gen-bidi-data.mjs
//
// bidi.ts consumes the data as:
//   - latin1BidiTypes[codePoint] for codePoint <= 0x00FF (direct index)
//   - binary search over nonLatin1BidiRanges ([start, end, type] tuples)
// The emitted header mirrors that shape 1:1.

import { writeFileSync, mkdirSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

const root = dirname(dirname(fileURLToPath(import.meta.url)));
// repo root: packages/pretext-core -> ../..
const bidiDataPath = join(
  dirname(dirname(root)),
  'node_modules/.pnpm/@chenglou+pretext@0.0.8/node_modules/@chenglou/pretext/dist/generated/bidi-data.js',
);

const { latin1BidiTypes, nonLatin1BidiRanges } = await import(bidiDataPath);

// Keep the enumerator order identical to the GeneratedBidiType union in
// src/generated/bidi-data.ts (order only matters for readability; bidi.ts
// compares types by equality, never by rank).
const BIDI_TYPES = ['L', 'R', 'AL', 'AN', 'EN', 'ES', 'ET', 'CS', 'ON', 'BN', 'B', 'S', 'WS', 'NSM'];

const typeIndex = new Map(BIDI_TYPES.map((t, i) => [t, i]));
function enumName(t) {
  if (!typeIndex.has(t)) throw new Error(`Unknown bidi type in data: ${t}`);
  return `BidiType::${t}`;
}

if (latin1BidiTypes.length !== 256) {
  throw new Error(`latin1BidiTypes expected 256 entries, got ${latin1BidiTypes.length}`);
}
for (const range of nonLatin1BidiRanges) {
  if (range.length !== 3 || !Number.isInteger(range[0]) || !Number.isInteger(range[1])) {
    throw new Error(`Malformed range: ${JSON.stringify(range)}`);
  }
}

let out = `// GENERATED by scripts/gen-bidi-data.mjs from @chenglou/pretext@0.0.8
// dist/generated/bidi-data.js (Unicode DerivedBidiClass-17.0.0). DO NOT EDIT.
// Formatting and isolate controls are projected onto BN because the rich
// bidi helper consumes a simplified class set and does not model UBA isolates.
#pragma once

#include <cstdint>

namespace pretext::tables {

// GeneratedBidiType from src/generated/bidi-data.ts.
enum class BidiType : uint8_t { ${BIDI_TYPES.join(', ')} };

// latin1BidiTypes: direct index by code point for codePoint <= 0x00FF.
inline constexpr BidiType kLatin1BidiTypes[256] = {
`;

for (let i = 0; i < 256; i += 8) {
  out +=
    '    ' +
    latin1BidiTypes
      .slice(i, i + 8)
      .map((t) => enumName(t))
      .join(', ') +
    ',\n';
}

out += `};

// nonLatin1BidiRanges: [start, end, type] tuples, sorted by start, searched
// with the binary search in bidi.cpp classifyCodePoint (mirrors bidi.ts).
struct BidiRange { uint32_t start, end; BidiType type; };

inline constexpr int32_t kNonLatin1BidiRangeCount = ${nonLatin1BidiRanges.length};
inline constexpr BidiRange kNonLatin1BidiRanges[${nonLatin1BidiRanges.length}] = {
`;

for (let i = 0; i < nonLatin1BidiRanges.length; i += 4) {
  out +=
    '    ' +
    nonLatin1BidiRanges
      .slice(i, i + 4)
      .map(([start, end, t]) => `{0x${start.toString(16)}, 0x${end.toString(16)}, ${enumName(t)}}`)
      .join(', ') +
    ',\n';
}

out += `};

}  // namespace pretext::tables
`;

const dest = join(root, 'cpp/pretext/tables/bidi_data.hpp');
mkdirSync(dirname(dest), { recursive: true });
writeFileSync(dest, out);
console.log(`wrote ${dest} (${nonLatin1BidiRanges.length} ranges)`);
