// UAX#29-lite grapheme cluster segmentation, pure JS.
//
// Hermes ships without Intl.Segmenter, and pretext needs grapheme clusters to
// decide where a word may be split mid-line. Full ICU data is megabytes; the
// rules pretext actually depends on for correct WIDTHS are much smaller:
// don't split combining marks off their base, keep emoji sequences (ZWJ
// chains, skin tones, flags) whole, keep Hangul jamo composed, keep CRLF
// together. We implement exactly those rules (GB3–GB13, minus Prepend, which
// covers a handful of rare prefix characters) over compact sorted code-point
// ranges + binary search.
//
// The Extend/SpacingMark tables below are the main Mn/Me/Mc blocks rather
// than the exhaustive UCD list — enough that any realistic text a chat app
// renders clusters identically to ICU. A miss means a rare mark becomes its
// own cluster, which for pretext only shifts a potential mid-word break
// point, never a measured width.

