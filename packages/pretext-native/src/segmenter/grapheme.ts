function inRanges(cp: number, ranges: readonly number[]): boolean {
  let lo = 0;
  let hi = (ranges.length >> 1) - 1;
  while (lo <= hi) {
    const mid = (lo + hi) >> 1;
    if (cp < ranges[mid * 2]!) hi = mid - 1;
    else if (cp > ranges[mid * 2 + 1]!) lo = mid + 1;
    else return true;
  }
  return false;
}

export function isExtendedPictographic(cp: number): boolean {
  return inRanges(cp, EXT_PIC_RANGES);
}

function gcbOf(cp: number): number {
  if (cp === 0x0d) return CR;
  if (cp === 0x0a) return LF;
  // Cc controls plus the Zl/Zp separators and ZWSP; ZWJ (200D) is its own
  // category and must not land here.
  if (cp < 0x20 || (cp >= 0x7f && cp <= 0x9f) || cp === 0x200b || cp === 0x2028 || cp === 0x2029) return CONTROL;
  if (cp === 0x200d) return ZWJ;
  if (cp >= 0x1f1e6 && cp <= 0x1f1ff) return RI;
  // Hangul is fully algorithmic — no tables needed. A precomposed syllable is
  // LV when its trailing-consonant index is zero, LVT otherwise.
  if (cp >= 0xac00 && cp <= 0xd7a3) return (cp - 0xac00) % 28 === 0 ? HANGUL_LV : HANGUL_LVT;
  if ((cp >= 0x1100 && cp <= 0x115f) || (cp >= 0xa960 && cp <= 0xa97c)) return HANGUL_L;
  if ((cp >= 0x1160 && cp <= 0x11a7) || (cp >= 0xd7b0 && cp <= 0xd7c6)) return HANGUL_V;
  if ((cp >= 0x11a8 && cp <= 0x11ff) || (cp >= 0xd7cb && cp <= 0xd7fb)) return HANGUL_T;
  if (inRanges(cp, EXTEND_RANGES)) return EXTEND;
  if (inRanges(cp, SPACING_MARK_RANGES)) return SPACING_MARK;
  return OTHER;
}

/**
 * Given that `start` is a cluster boundary, return the index (UTF-16 code
 * units) of the next boundary. All the state GB11/GB12 need is cluster-local,
 * so starting fresh at a boundary is sound — that's what lets the word
 * segmenter call this for one-off clusters without a full scan.
 */
export function nextGraphemeBoundary(text: string, start: number): number {
  const len = text.length;
  let i = start;
  let cp = text.codePointAt(i)!;
  let prev = gcbOf(cp);
  // GB11 state machine: 0 = not in an emoji sequence, 1 = pictograph base
  // seen (Extends may follow), 2 = that sequence just consumed a ZWJ and a
  // following pictograph should join.
  let emojiSeq: 0 | 1 | 2 = isExtendedPictographic(cp) ? 1 : 0;
  // GB12/13: count of consecutive Regional Indicators ending at `prev` —
  // flags join in pairs, so only an odd count admits one more RI.
  let riCount = prev === RI ? 1 : 0;
  i += cp > 0xffff ? 2 : 1;

  while (i < len) {
    cp = text.codePointAt(i)!;
    const cur = gcbOf(cp);
    const curIsExtPic = isExtendedPictographic(cp);

    let breaks: boolean;
    if (prev === CR && cur === LF) breaks = false; // GB3
    else if (prev === CONTROL || prev === CR || prev === LF) breaks = true; // GB4
    else if (cur === CONTROL || cur === CR || cur === LF) breaks = true; // GB5
    else if (prev === HANGUL_L && (cur === HANGUL_L || cur === HANGUL_V || cur === HANGUL_LV || cur === HANGUL_LVT))
      breaks = false; // GB6
    else if ((prev === HANGUL_LV || prev === HANGUL_V) && (cur === HANGUL_V || cur === HANGUL_T))
      breaks = false; // GB7
    else if ((prev === HANGUL_LVT || prev === HANGUL_T) && cur === HANGUL_T) breaks = false; // GB8
    else if (cur === EXTEND || cur === ZWJ) breaks = false; // GB9
    else if (cur === SPACING_MARK) breaks = false; // GB9a
    else if (prev === ZWJ && curIsExtPic && emojiSeq === 2) breaks = false; // GB11
    else if (prev === RI && cur === RI && riCount % 2 === 1) breaks = false; // GB12/13
    else breaks = true; // GB999

    if (breaks) return i;

    riCount = cur === RI ? riCount + 1 : 0;
    if (curIsExtPic) emojiSeq = 1;
    else if (emojiSeq === 1 && cur === EXTEND) emojiSeq = 1;
    else if (emojiSeq === 1 && cur === ZWJ) emojiSeq = 2;
    else emojiSeq = 0;
    prev = cur;
    i += cp > 0xffff ? 2 : 1;
  }
  return len;
}

export function* iterateGraphemes(text: string): Generator<{ segment: string; index: number }> {
  let i = 0;
  while (i < text.length) {
    const end = nextGraphemeBoundary(text, i);
    yield { segment: text.slice(i, end), index: i };
    i = end;
  }
}
